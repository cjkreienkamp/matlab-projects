function TF = C2F(TC)

% Chris Kreienkamp
% Section 10
% 2/21/17
%
% Output variable is TF
% The name of the file AND function are the same - C2F
% Input variable TC
%
% C2F(TC) takes the input of a temperature in Celsius and
% returns the temperature in Fahrenheit

% Unit conversion equation
TF = 1.8*TC + 32;